CODE SEGMENT
 ASSUME CS:CODE
START:

RESTART:
MOV AH,07H
INT 21H
CMP AL,0DH
JE ENDD;回车则结束程序

CMP AL,2FH
JBE RESTART;除去回车，ASCII码在0~47则重新开始
CMP AL,39H
JBE NUM;ASCII码在48~57，进入数字处理块
CMP AL,40H
JBE RESTART;ASCII码在58~64则重新开始
CMP AL,5AH
JBE ALPHA;ASCII码在65~90，进入字母处理块
CMP AL,60H
JBE RESTART;ASCII码在91~96则重新开始
CMP AL,7AH
JBE ALPHA;ASCII码在97~122，进入字母处理块
JMP RESTART;ASCII码在123~126则重新开始

NUM:
MOV DL,AL
JMP DISPLAY
;实现对输入数字的显示

ALPHA:
MOV DL,63H
JMP DISPLAY
;63H对应'c'的ASCII码，实现输入字母显示'c'

DISPLAY:
MOV AH,02H
INT 21H
JMP RESTART
;使用int指令输出(DL)对应字符，输出结束后重新开始

ENDD:
MOV AH,4CH
INT 21H 
CODE ENDS
END START
